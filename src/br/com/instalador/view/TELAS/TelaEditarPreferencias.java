    public class ProcessoATUALIZA_CERTS implements Runnable {

        @Override
        public void run() {
            File caminho = new File(C_PASTA.getText().trim());
            if (caminho.exists() & caminho.isDirectory()) {
                CERTIFICADOS_PASTA updateCertsBD = new CERTIFICADOS_PASTA();
                updateCertsBD.atualizarCertificadosBD(C_PASTA.getText().trim(),
                        C_SENHA_CERT.getText().trim(), controle);
            } else {
                JOptionPane.showMessageDialog(null, "A PASTA INFORMADA É INVÁLIDA OU ESSE COMPUTADOR"
                        + " NÃO POSSUI ACESSO À ELA!");
                BT_UPDADE_CERTS.setEnabled(true);
                BT_EDIT.setEnabled(true);
                BT_SALVAR.setEnabled(true);
            }
        }
    }


    private void BT_EDITActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BT_EDITActionPerformed
        if (C_PASTA.isEnabled() && C_SENHA_CERT.isEnabled()) {
            C_PASTA.setEnabled(false);
            C_SENHA_CERT.setEnabled(false);
            C_PASTA.setEditable(false);
            C_SENHA_CERT.setEditable(false);
        } else {
            C_PASTA.setEnabled(true);
            C_SENHA_CERT.setEnabled(true);
            C_PASTA.setEditable(true);
            C_SENHA_CERT.setEditable(true);
        }
    }//GEN-LAST:event_BT_EDITActionPerformed

    private void BT_SALVARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BT_SALVARActionPerformed
        //
        C_PASTA.setEnabled(false);
        C_SENHA_CERT.setEnabled(false);
        C_PASTA.setEditable(false);
        C_SENHA_CERT.setEditable(false);
        //      
        controle.atualizarPreferencias(C_PASTA.getText().trim(), C_SENHA_CERT.getText().trim());
        JOptionPane.showMessageDialog(null, "Preferências Atualizadas!");

        atualizaTela();
    }//GEN-LAST:event_BT_SALVARActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser fc1 = new JFileChooser();
        fc1.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int res1 = fc1.showOpenDialog(null);
        if (res1 == JFileChooser.APPROVE_OPTION) {
            File diretorio1 = fc1.getSelectedFile();
            File url1 = new File(diretorio1.getPath());
            if (url1.exists()) {
                C_PASTA.setText(url1.getAbsolutePath());
            } else {
                JOptionPane.showMessageDialog(null, "SELECIONE A PASTA QUE CONTÉM OS CERTIFICADOS!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void BT_UPDADE_CERTSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BT_UPDADE_CERTSActionPerformed

        int response = JOptionPane.showConfirmDialog(null, "DESEJA PROCESSAR OS CERTIFICADOS DA PASTA "
                + "(" + C_PASTA.getText() + ")? ", "Confirma?",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (response == JOptionPane.NO_OPTION) {
            JOptionPane.showMessageDialog(null, "CANCELADO!");
        } else {
            if (response == JOptionPane.YES_NO_OPTION) {
                BT_UPDADE_CERTS.setEnabled(false);
                BT_EDIT.setEnabled(false);
                BT_SALVAR.setEnabled(false);
                BT_DELETA_VENCIDOS.setEnabled(false);
                //
                ProcessoATUALIZA_CERTS procUpdateCerts = new ProcessoATUALIZA_CERTS();
                Thread threadUpdatCerts = new Thread(procUpdateCerts);
                threadUpdatCerts.setName("Thread threadUpdatCerts");
                threadUpdatCerts.start();
            }
        }
    }//GEN-LAST:event_BT_UPDADE_CERTSActionPerformed

    private void BT_DELETA_VENCIDOSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BT_DELETA_VENCIDOSActionPerformed
        int response = JOptionPane.showConfirmDialog(null, "DESEJA DELETAR TODOS OS CERTIFICADOS VENCIDOS? ", "Confirma?",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (response == JOptionPane.NO_OPTION) {
            JOptionPane.showMessageDialog(null, "EXCLUSÃO CANCELADA!");
        } else {
            if (response == JOptionPane.YES_NO_OPTION) {

                if (controle.deletarCertificadosVencidos()) {
                    JOptionPane.showMessageDialog(null, "OS CERTIFICADOS VENCIDOS FORAM EXCLUÍDOS!");
                }
            }
        }
    }//GEN-LAST:event_BT_DELETA_VENCIDOSActionPerformed

